// ── UTILITY FUNCTIONS ─────────────────────────────────────────

/**
 * Generates a simple unique-enough ID with a prefix.
 * Not cryptographic — fine for localStorage keys.
 */
function generateId(prefix = 'id') {
  return `${prefix}_${Date.now()}_${Math.random().toString(36).slice(2, 7)}`;
}

/** Returns today's date as 'YYYY-MM-DD' in local time. */
function todayISO() {
  const d = new Date();
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`;
}

/**
 * Formats an ISO date string for display.
 * e.g. '2024-03-15' → 'Fri Mar 15'
 */
function formatDateDisplay(isoString) {
  if (!isoString) return '';
  const [y, m, d] = isoString.split('-').map(Number);
  const date = new Date(y, m - 1, d);
  return date.toLocaleDateString('en-US', { weekday: 'short', month: 'short', day: 'numeric' });
}

/**
 * Formats seconds into a human-readable duration.
 * e.g. 90 → '1:30', 45 → '0:45'
 */
function formatDuration(seconds) {
  const m = Math.floor(seconds / 60);
  const s = seconds % 60;
  return `${m}:${String(s).padStart(2, '0')}`;
}

/**
 * Returns the stage object for a given stage id.
 * Defaults to DEAD_HANG_BASE if not found.
 */
function getStageById(stageId) {
  return Object.values(PULL_UP_STAGES).find(s => s.id === stageId)
    || PULL_UP_STAGES.DEAD_HANG_BASE;
}
